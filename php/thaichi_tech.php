		<h4>Общие рекомендации</h4>


		<p>Голова держится прямо, подбородок при этом чуть поджат. Должно быть такое ощущение, будто волосы на макушке тянут вверх. При этом создается впечатление, что голова находится в “подвешенном” состоянии. <br>
		Плечи естественно опущены и расслаблены. Руки округлые. Локти провисшие и немного согнуты. Пальцы рук естественно распрямлены. При формировании кисти в кулак или “крюк” пальцы также не напрягаются. <br>
		Тело естественно вытянуто и расслаблено, отдавая приоритет гибкости. <br>
		Талия расслаблена. Нужно запомнить, что все движения “выходят” из талии: <br>
		Ноги округлые. Бедра естественно напряжены. В позиции “лук-стрела” задняя нога полностью не выпрямляется. Колено передней ноги не выходит за вертикальную линию носка. </p><br>


		<h4>Динамика перемещений</h4>


		<p>Выполнять движения следует легко и непринужденно, сохраняя устойчивость тела на всем протяжении упражнений. Движения подобны плывущим облакам: спокойны и текучие. Мышцы расслаблены. В Китае говорят: “Когда выносите вперед силу, напряжение так слабо, что его можно сравнить с разматыванием необработанного шелка с кокона”. Во время занятий тайцзицюань необходимы гибкость и естественность. Это не означает, что исполнение должно быть неточным и безжизненным. Передвигаться нужно легко и мягко. Некоторые классические учителя описывали перемещения так: “Когда делаете шаг, это должно быть так тихо, словно ходит кошка”.<br>
		Необходимо осознать, что все движения направляются поясницей и проявляются в конечностях. Поэтому очень важно координировать работу ног, талии и рук, так как в тайцзицюань они представляются единым целым. <br>
		Каждая часть тела наводится в постоянном движении. Выполняющий не должен вести себя подобно марионетке. Нельзя фокусировать внимание только на руках, пренебрегая талией и ногами, которые являются главными несущими частями. <br>
		Дышать необходимо животом, то есть диафрагмой. Дыхание ритмичное, ровное, глубокое и координируется с “открытыми” и “закрытыми” движениями. “Открытые” движения обычно связаны с перемещениями вперед или разведением конечностей в стороны. “Закрытые” основываются на отходах или сведении конечностей. Выдох выполняется на “открытом”, вдох — на “закрытом” движении. <br>
		Первоначально следует темп выполнения упражнений сочетать с естественным дыханием. Со временем, когда дыхание станет глубоким и продолжительным, темп выполнения упражнений замедляется. Необходимо комбинировать “полноту” и “пустоту” и правильно чередовать их. “Полнота” и “пустота” связана с сознательной концентрацией мысли и контроле того или иного действия. При этом необходимо соблюдать полное сосредоточение и спокойствие, учитывая, что мысль “определяет движение”. При выполнении движений взгляд следует за руками или направлен вперед. Так как концентрации мысли в тайцзицюань придается особое значение, возникает возможность регулирования физиологических процессов организма, поэтому некоторые специалисты сравнивают тайцзицюань с “физическими упражнениями сознания” или “терапевтическими упражнениями пустой головы”. Изучать комплекс “24 формы” необходимо постепенно, последовательно осваивая всю серию форм. Комплекс “24 формы” выполняется в течение 4—6 минут. Количество упражнений для каждого занятия устанавливается по состоянию здоровья и количеству располагаемого времени. Допускается выполнение одной или нескольких частей и непрерывной последовательности. Лучшее время для занятий — утро, однако вечерние тренировки также пойдут на пользу. </p>


После шести лет обучения Ян Лучань возвращается в родной уезд и начинает преподавание. В то же время он работает над развитием стиля. 
Так, постепенно меняя характер движений, он делает их более плавными и растянутыми. Стиль приобретает всё более возрастающее
 оздоровительное значение, что позволило одному из учеников Ян Лучаня написать: «Что является высшей целью тайцзицюань? Сохранение здоровья и продление жизни».

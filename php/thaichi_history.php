		<h2>История тайцзи</h2>
		<p>Тайцзицюа́нь (тайчи) (кит. трад. 太極拳, упр. 太极拳, пиньинь: tàijíquán) — буквально: «кулак Великого Предела»; китайскоевнутреннее боевое искусство, один из видов ушу (происхождение тайцзицюань — исторически спорный вопрос, разные источники имеют разные версии). Популярно как оздоровительная гимнастика, но приставка «цюань» (кулак) подразумевает, что тайцзицюань — это боевое искусство. По некоторым данным, ушу, каратэ, бокс, танцы и йога имеют в себе корни от тайцзицюань.</p>
		<img src="../images/img1.png" class="woman" alt="тайцзи">
		
		<p>История возникновения тайцзицюань — спорная тема, так как в различные моменты времени были различные официальные точки зрения, что способствовало распространению различных, не слишком правильных, а порой и полностью ошибочных интерпретаций. </p>
		<p>Существует две конкурирующие версии древней истории тайцзицюань. Одна из них, которая сегодня является официальной версией китайского правительства, считает, что это боевое искусство развивалось внутри семьи Чэнь, с XIV века проживавшей в деревне Чэньцзягоу уезда Вэньсянь северокитайской провинции Хэнань, и что основал его в XVII веке Чэнь Вантин, от которого можно проследить непрерывную линию передачи традиции. </p>
		<p>Другая, более древняя версия, которой придерживаются представители стиля Ян, У, Хао и Сунь, говорит о том, что патриархом Тайцзицюань является легендарный даосский отшельник Чжан Саньфэн, однако эта версия изобилует натяжками и совсем не объясняет, как и через кого это боевое искусство передавалось до XIX века. </p>
		<img src="../images/img2.png" class="man" alt="тайцзи">
		<p>Согласно современным исследованиям первые упоминания о боевых техниках, подобных Тайцзицюань связываются с даосом Сюй Сюаньпином (618—907 гг. н. э., династия Тан), приёмы которого имели названия, абсолютно схожие с сегодняшними названиями некоторых форм («Хлыст», «Играть на пипе» и т. д.). Его искусство боя развивалось и передавалось устно среди даосов-отшельников. Называли эти техники по-разному, общими оставались принципы и требования к исполнению, которые впервые изложил в «Классическом тексте по тайцзицюань» Чжан Саньфэн (960—1279 гг. н. э., династия Сун). Существует множество историй и легенд о том, как Чжан Саньфэн создал тайцзицюань. По легенде, патриарх родился в девятый день четвёртой луны 1247 года (этот день по всему миру отмечают как день рождения тайцзицюань) и прожил, по преданию, более 200 лет. </p>
		<p>Следующая замечательная личность в ряду передающих традицию — Ван Цзунъюэ, живший в династию Мин (1368—1644 гг. н. э.). Он был известным полководцем и оставил после себя тексты «Руководство по тайцзицюань», «Разъяснение духовной сути 13 форм» и «Об Истинном свершении», которые вместе с трактатом Чжан Саньфэна составляют классическое наследие Тайцзицюань. Считается, что от Ван Цзунъюэ через Цзян Фа традиция была передана Чэнь Чжансину из рода Чэнь, представители которого с 1949 года стали продвигать другую версию истории этого искусства. Чэнь Чжансин передал искусство Ян Лучаню, а через Яна искусство унаследовали его сыновья и внуки, а также многие другие впоследствии известные мастера. </p>
	

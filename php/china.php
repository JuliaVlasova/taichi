	<p>Кита́й (кит. трад. 中國, упр. 中国, пиньинь: Zhōngguó, палл.: Чжунго), официальное название — Кита́йская Наро́дная Респу́блика (КНР) 
	(кит. трад. 中華人民共和國, упр. 中华人民共和国, пиньинь: Zhōnghuá Rénmín Gònghéguó, палл.: Чжунхуа Жэньминь Гунхэго) — 
	социалистическое государство в Восточной Азии. Крупнейшее по численности населения государство мира (свыше 1,36 млрд, 
	большинство населения — этнические китайцы, самоназвание — хань); занимает третье место в мире по территории, уступая России и Канаде. </p>
	<p>Является великой державой — кандидатом-сверхдержавой, экономической сверхдержавой, постоянный член Совета безопасности ООН. 
	Одна из ведущих космических держав мира, обладает ядерным оружием и крупнейшей в мире армией по численности военнослужащих. </p>
	<p>В 2010 году китайская экономика обогнала японскую и стала второй экономикой мира по номинальному ВВП. С декабря 2014 
	года является первой экономикой мира по ВВП (ППС). КНР является мировым лидером по производству большинства видов 
	промышленной продукции, в том числе по производству автомобилей и потребительскому спросу на них. Крупнейший мировой 
	экспортёр («фабрика мира»). Располагает наибольшими в мире золотовалютными резервами. </p>
	<p>Китай состоит в таких международных организациях, как ООН, АТЭС, G20, Всемирная торговая организация (ВТО; с декабря 2001 
	года), а также ШОС и БРИКС. </p>
	<p>Со времени провозглашения Китайской Народной Республики в 1949 году правящей партией является Коммунистическая партия Китая (КПК). </p>
	<p>История Китая особенна разнообразием письменных источников, которые, в сочетании с археологическими данными, позволяют 
	реконструировать политическую жизнь и социальные процессы, начиная с глубокой древности. Канонизированные древнекитайские 
	тексты религиозно-философского и исторического содержания — особенно те из них, в которых излагалось учение Конфуция — 
	в свою очередь, сами влияли на дальнейшее развитие китайской цивилизации, мировоззрение китайского народа. </p>
	<p>Для китайской цивилизации со времён Конфуция была характерна высокая социально-политическая активность каждого человека,
	 направленная на достижение счастья и благополучия именно в земной жизни — при этом судьба его зависела не от божественного 
	 предопределения, а от собственных усилий. </p>
	<p>Китайская цивилизация — одна из старейших в мире. По утверждениям китайских учёных, её возраст может составлять
	пять тысяч лет, при этом имеющиеся письменные источники покрывают период не менее 3500 лет. Давнее наличие систем административного 
	управления, которые совершенствовались сменявшими друг друга династиями, создавало очевидные преимущества для китайского государства,
	экономика которого основывалась на развитом земледелии, по сравнению с более отсталыми соседями-кочевниками и горцами. 
	Ещё более укрепило китайскую цивилизацию введение конфуцианства в качестве государственной идеологии (I век до н. э.) 
	и единой системы письма (II век до н. э.). </p>

	<p>С политической точки зрения Китай в течение нескольких тысячелетий проходил через циклически повторяющиеся периоды 
	политического единства и распада, причём в ряде случаев частично или полностью становился частью иностранных государств 
	(например, империй Юань и Цин). Территория Китая регулярно подвергалась нашествиям извне, однако большинство захватчиков 
	рано или поздно подвергались китаизации и ассимилировались в китайский этнос, а территории их государств обычно включались
	в территорию Китая. Современное китайское государство и общество является результатом многовекового культурного и 
	политического взаимопроникновения и взаимодействия с многочисленными окружающими азиатскими народами, 
	сопровождавшегося перемещениями многомиллионных людских масс и многовековой ханьской ассимиляцией. При этом необходимо
	учитывать, что традиционная китайская историография включает суверенные государства других народов (монголов, маньчжуров и др.) 
	в хроникально-династийную историю Китая, игнорируя собственные исторические традиции этих народов и рассматривая их государства 
	как исторические части Китая. </p>

